    @media (hover: none) and (pointer: coarse) {
        [data-projects-section] article.group img {
            transition: filter 900ms cubic-bezier(.2, .6, .2, 1);
        }

        [data-projects-section] article.group h3 {
            transition: color 500ms cubic-bezier(.2, .6, .2, 1);
        }
    }
